        let nextButton = document.getElementById('next');
        let prevButton = document.getElementById('prev');
        let closeDetailBtn = document.getElementById('closeDetail');
        let seeMoreButtons = document.querySelectorAll('.seeMore');
        let carousel = document.querySelector('.carousel');
        let listHTML = document.querySelector('.carousel .list');
        let detailPage = document.getElementById('detailPage');
        let detailContent = document.getElementById('detailContent');

        nextButton.onclick = function(){
            showSlider('next');
        }

        prevButton.onclick = function(){
            showSlider('prev');
        }

        let unAcceptClick;
        const showSlider = (type) => {
            nextButton.style.pointerEvents = 'none';
            prevButton.style.pointerEvents = 'none';

            carousel.classList.remove('prev', 'next');
            let items = document.querySelectorAll('.carousel .list .item');
            if (type === 'next'){
                listHTML.appendChild(items[0]);
                carousel.classList.add('next');
            } else {
                let positionLast = items.length - 1;
                listHTML.prepend(items[positionLast]);
                carousel.classList.add('prev');
            }

            clearTimeout(unAcceptClick);
            unAcceptClick = setTimeout(() => {
                nextButton.style.pointerEvents = 'auto';
                prevButton.style.pointerEvents = 'auto';
            }, 2000);
        }

        // Handle See More buttons
        function attachSeeMoreListeners() {
            let seeMoreButtons = document.querySelectorAll('.seeMore');
            seeMoreButtons.forEach(button => {
                button.onclick = function(e){
                    e.preventDefault();
                    let item = this.closest('.item');
                    let memberKey = item.getAttribute('data-member');
                    showDetail(memberKey);
                }
            });
        }

        function showDetail(memberKey) {
            let member = memberData[memberKey];
            
            let projectsHTML = member.projects.map(project => `
                <div class="project-card">
                    <h3>${project.title}</h3>
                    <p>${project.desc}</p>
                </div>
            `).join('');

            let educationHTML = member.education.map(edu => `
                <div class="education-item">
                    <h3>${edu.school}</h3>
                    <div class="year">${edu.year}</div>
                    <p>${edu.degree}</p>
                </div>
            `).join('');

            let certificatesHTML = member.certificates.map(cert => `
                <div class="certificate-item">
                    <h3>${cert.title}</h3>
                    <div class="year">${cert.year}</div>
                    <p>Issued by: ${cert.issuer}</p>
                </div>
            `).join('');

            detailContent.innerHTML = `
                <div class="detail-header">
                    <img src="${member.image}" alt="${member.name}">
                    <div class="detail-header-info">
                        <h1>${member.name}</h1>
                        <div class="stage-name">${member.stageName}</div>
                    </div>
                </div>

                <div class="detail-section">
                    <h2>About Me</h2>
                    <p>${member.about}</p>
                </div>

                <div class="detail-section">
                    <h2>Projects & Works</h2>
                    <div class="projects-grid">
                        ${projectsHTML}
                    </div>
                </div>

                <div class="detail-section">
                    <h2>Educational Background</h2>
                    ${educationHTML}
                </div>

                <div class="detail-section">
                    <h2>Certificates & Achievements</h2>
                    ${certificatesHTML}
                </div>
            `;

            detailPage.classList.add('active');
            document.body.style.overflow = 'hidden';
        }

        closeDetailBtn.onclick = function() {
            detailPage.classList.remove('active');
            document.body.style.overflow = 'auto';
        }

        // Initial attachment
        attachSeeMoreListeners();

        // Re-attach listeners after slider moves
        const observer = new MutationObserver(() => {
            attachSeeMoreListeners();
        });

        observer.observe(listHTML, { childList: true });